let i = 1;

// while

while (i <= 100) {
	console.log('Skaičius iki 100 su "while": ' + i);
	
	i++;
	
}
// // do

let y = 1;

do {
	console.log('Skaičius iki 100 su "do": ' + y);
	
	y++;

} while (y <= 100);

// for

for (let x = 0; x <= 100; x++) {
	console.log('Skaičius iki 100 su "for": ' + x);
}